# Ingresando las variables de las frutas
manzana = 100
print(f"Precio de la manzana: {manzana} $")
pera = 250
print(f"Precio de la pera: {pera} $")
durazno = 300
print(f"Precio del durazno: {durazno} $")

# Ingresando las variables de cantidades
cantidadmanzana = 150
print(f"Cantidad de manzanas: {cantidadmanzana}")
cantidadpera = 80
print(f"Cantidad de peras: {cantidadpera}")
cantidaddurazno = 120
print(f"Cantidad de duraznos: {cantidaddurazno}")

# Calculando el total a pagar de las frutas
totalmanzana = (manzana * cantidadmanzana)
totalpera = (pera * cantidadpera)
totaldurazno = (durazno * cantidaddurazno)
print(f"El total a pagar de las manzanas es {totalmanzana} $")
print(f"El total a pagar de las peras es {totalpera} $")
print(f"El total a pagar de los duraznos es {totaldurazno} $")

# Realizando operaciones
sumatotal = (totalmanzana + totalpera)
print(f"La suma total a pagar por la compra de las manzanas y peras es de: {sumatotal} $")